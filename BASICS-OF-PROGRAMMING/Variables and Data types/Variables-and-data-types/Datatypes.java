// There are two types of data types in java ========

// 1. Primitive ========                                     2.Non-Primitive ========

//     byte       1 byte    (8-bits)                         String
//     short      2 byte    (16-bits)                        Array
//     char       2 byte    (16-bits)                         Class
//     boolean    1 byte    (8-bits)                          Object
//     int        4 byte    (32-bits)                         Interface
//     long       8 byte    (64-bits)
//     float      4 byte    (32-bits)
//     double     8 byte    (64-bits)


// Number Type or Integer group________________ This group include byte, short, int, long

// byte  
// value range from -128 to 127 
// default value is 0
// example byte b = 10

// short
// value range from -32768 to 32767 
// default value is 0
// example short s = 11

// int 
// value range from -2147483648 to 2147483647 
// default value is 0
// example int i = 10

// long 
// value range from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
// default value is 0
// example long l = 100012



// Floating Data Types___________________________

// float
// Default value is 0.0f
// Example db = 10.3f

// double
// Default value is 0.0d
// Example db = 11.123


//Character data type _________________________________

// -> Character data type is used to store single character
// -> Any single character (alphabet / digit / special character) we can store using 'char' data
// type
// -> char datatype will occupy 2 bytes of memory
// -> When we are storing data into 'char' data type single quote is mandatory

//Char______________________
// It is of 2 bytes unasigned unicode character.abstract
// example char c = 'a'


// Boolean data type___________________________________________

// -> It is used to store true or false values only
// -> It will occupy 1 bit memory
